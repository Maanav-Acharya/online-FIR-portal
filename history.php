<p style="font-size:20px;width:1100px;margin-left:25px;background-color:white;">&emsp;DID YOU KNOW THE MUMBAI POLICE FORCE IN ITS EARLY AVATAR WAS FORMED BY A UNIT CALLED THE BHANDARI MILITIA.
System of modern policing in Mumbai began with the establishment of a Police Out-post by the Portuguese in 1661.
<br>&emsp;Later in the year 1669 when the East India Company acquired Mumbai, Gerald Aungier became the Governor of the City and is considered the founder of the city's present Police Force. He organized the Bhandari Militia with Subhedars head quartered at Mahim, Sewree and Sion. He died at Surat in 1677.</p>

<p style="font-size:20px;width:1100px;margin-left:25px;background-color:white;">&emsp;On February 17, 1779, Mr. James Tod was appointed as "Lieutenant of Police" on probation with an allowance of Rs. 4 per diem and on March 3rd of that year he was sworn in to office. Tod had a chequered career as head of the Bombay Police. Tod's regulations, which numbered 41, were the only rules for the management of the force which had been passed up to that date in a formal manner. He had twice been indicted for felony and had been honorably acquitted on both occasions: but he still lived in continual dread of blame.</p>

<p style="font-size:20px;width:1100px;margin-left:25px;background-color:white;">&emsp;In the year 1793, Act XXXIII, Geo. III was promulgated. The post of Deputy of Police and High Constable was abolished and in its place post of Superintendent of Police was created. Mr. Simon Halliday was the first Superintendent of Police. He was in-charge of his office until 1808. He had made a thorough revision of and re-arrangement for policing, the area out side the Fort. The troublesome area known as "Dungree and the Woods" was split up into 14 Police divisions, each division being staffed by two constables (European) and varying number of Peons</p>
